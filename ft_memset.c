#include "libft.h"

void	*ft_memset(void *m, int c, size_t n)
{
	char	*s;
	size_t	i;

	s = (char *)m;
	i = 0;
	while (i < n)
	{
		s[i] = c;
		i++;
	}
	return (m);
}
